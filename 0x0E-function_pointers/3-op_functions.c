#include "3-calc.h"

/**
 * op_add - add two integers
 *
 * @a: input integer
 *
 * @b: input integer
 *
 * Return: integer result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 *
 * @a: input integer
 *
 * @b: input integer
 *
 * Return: integer result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integers
 *
 * @a: input integer
 *
 * @b: input integer
 *
 * Return: integer result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integers
 *
 * @a: input integer
 *
 * @b: input integer
 *
 * Return: integer result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modular of two integers
 *
 * @a: input integer
 *
 * @b: input integer
 *
 * Return: integer result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
